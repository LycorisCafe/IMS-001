    private void dataGrab() {
        try ( Stream<String> lines = Files.lines(
                Paths.get("C:\\ProgramData\\LycorisCafe\\IMS\\Temp\\StudentId.lc"))) {
            jTextField2.setText(lines.skip(0).findFirst().get());
        } catch (IOException ex) {
            System.out.println(ex);
        }
        String subject;
        String grade;
        String subjectId;
        String teacherId;
        String classId;
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        try {
            Connection con = Helper.DB.connect();
            Statement stmt = con.createStatement();
            ResultSet rs = stmt.executeQuery("SELECT * "
                    + "FROM regclass "
                    + "WHERE studentId='" + jTextField2.getText() + "'");
            while (rs.next()) {
                classId = rs.getString("classId");
                ResultSet rs2 = stmt.executeQuery("SELECT * "
                        + "FROM classes "
                        + "WHERE id='" + classId + "'");
                while (rs2.next()) {
                    teacherId = rs2.getString("teacherId");
                    subjectId = rs2.getString("subjectId");
                    ResultSet rs3 = stmt.executeQuery("SELECT * "
                            + "FROM subjects "
                            + "WHERE id='" + subjectId + "'");
                    while (rs3.next()) {
                        grade = rs3.getString("grade");
                        subject = rs3.getString("subject");
                        ResultSet rs4 = stmt.executeQuery("SELECT * "
                                + "FROM teachers "
                                + "WHERE id='" + teacherId + "'");
                        while (rs4.next()) {
                            Object[] row = {classId, grade, subject,
                                rs4.getString("name")};
                            model.addRow(row);
                        }
                    }
                }
            }
            con.close();
        } catch (SQLException e) {
        }
    }

